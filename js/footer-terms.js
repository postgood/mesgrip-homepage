function initFooterModals() {
    // 푸터 모달 링크 클릭 핸들러
    $(document).on('click', '.footer-modal-link', function(e) {
        e.preventDefault();
        const modalType = $(this).data('modal');
        const termData = footerTermsData[modalType];
        
        if (termData) {
            $('#modalTitle').text(termData.title);
            $('#modalBody').html(termData.content);
            $('#termsModal').addClass('active');
            $('body').css('overflow', 'hidden');
        }
    });
}

// Export for use in app.js
if (typeof window !== 'undefined') {
    window.initFooterModals = initFooterModals;
}

